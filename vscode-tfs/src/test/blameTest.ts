/**
 * 测试文件 - 用于测试标注功能
 * 此文件用于测试 TFS 扩展的标注功能
 */

// 用于测试标注功能的简单函数
function helloWorld(): string {
    return "Hello, World!";
}

// 用于测试标注功能的另一个函数
function goodbyeWorld(): string {
    return "Goodbye, World!";
}

// 主函数
function main(): void {
    console.log(helloWorld());
    console.log(goodbyeWorld());
}

// 运行主函数
main();